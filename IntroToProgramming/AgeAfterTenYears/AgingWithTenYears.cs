using System;

class AgingWithTenYears
{
    static void Main()
    {
        int MyAge = 35;
        double MyAgeInTenYears = (MyAge + 10);

        Console.WriteLine("I am " + MyAge + " Years old.");
        Console.WriteLine("In 10 years I'll be " + MyAgeInTenYears + " years old.");
        Console.ReadLine();
    }
}

